namespace docker.worker.manager.SharedModel.UserService
{
    /// <summary>
    ///     Represents the user company details.
    /// </summary>
    public class Company
    {
        /// <summary>
        ///     Gets or sets the user company name.
        /// </summary>
        public string Name { get; set; }

        /// <summary>
        ///     Gets or sets the user company catch phrase.
        /// </summary>
        public string CatchPhrase { get; set; }

        /// <summary>
        ///     Gets or sets the user company BS.
        /// </summary>
        public string Bs { get; set; }
    }
}
